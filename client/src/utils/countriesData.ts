const countryMap = new Map(COUNTRIES.map(c => [c.code, c]));

export function getCountryInfo(code: string): CountryInfo | undefined {
  return countryMap.get(code);
}

export function getRegionForCountry(code: string): string | undefined {
  return countryMap.get(code)?.region;
}

export function getRegionsForCountries(codes: string[]): string[] {
  if (codes.includes('GLOBAL')) {
    return Object.keys(REGIONS);
  }
  const regions = new Set<string>();
  for (const code of codes) {
    const region = getRegionForCountry(code);
    if (region) regions.add(region);
  }
  return Array.from(regions);
}
